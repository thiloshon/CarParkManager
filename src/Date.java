public class Date {
    int year;
    int month;
    int day;
    int hour;
    int minute;

    public Date(int day, int hour, int minnute, int month, int year) {
        this.day = day;
        this.hour = hour;
        this.minute = minnute;
        this.month = month;
        this.year = year;
    }
}
